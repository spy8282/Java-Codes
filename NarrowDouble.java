class NarrowDouble
{
	public static void main(String[] args) 
	{
		byte a = (double) 100;
		short b =(double) 200;
		char c = (double) 'c';
		int d =  (double) 400;
		long e = (double) 500;
		float f =(double) 600.0f;
		double g = 600.0;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		
			
		
		
	}
}
