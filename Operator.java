class Operator
{
    public static void main(String[] args)
    {
        byte a = 30;
        byte b = 15;
        int prod1 = a%b;
        System.out.println(prod1);
		
		short c = 300;
        short d = 150;
        int prod2 = c%d;
        System.out.println(prod2);
		
		char e = 'e';
        char f = 'f';
        int prod3 = e%f;
        System.out.println(prod3);
		
		int g = 3000;
        int h = 1500;
        int prod4 = g%h;
        System.out.println(prod4);
		
		long i = 50000l;
        long j = 20000l;
        long prod5 = i%j;
        System.out.println(prod5);
		
		float k = 5.0f;
        float l = 2.0f;
        float prod6 = k%l;
        System.out.println(prod6);
		
		
    }
}